
public class TresVariablesAPP {

	public static void main(String[] args) {
		
		int N=5;
		double A=4.56;
		char C='\u0061';
		
		System.out.println("Variable N = "+N);
		System.out.println("Variable A = "+A);
		System.out.println("Variable C = "+C);
		
		double suma=N+A;
		double resta=A-N;
		int asciiValue='a';
		
		System.out.println("5 + 4.56 = "+suma);
		System.out.println("4.56 - 5 = "+resta);
		System.out.println("Valor numérico del carácter a = "+asciiValue);

	}

}
